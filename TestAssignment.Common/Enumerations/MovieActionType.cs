namespace TestAssignment.Common.Enumerations;

/// <summary>
/// Represents the type of action performed on a movie.
/// </summary>
public enum MovieActionType
{
    /// <summary>
    /// Indicates a click action on a movie.
    /// </summary>
    ActionClick,

    /// <summary>
    /// Indicates a view action on a movie.
    /// </summary>
    ActionView,

    /// <summary>
    /// Indicates a search action for a movie.
    /// </summary>
    ActionSearch,
}
